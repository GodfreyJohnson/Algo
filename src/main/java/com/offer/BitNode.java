package com.offer;

public class BitNode {
  int data;
  BitNode left;
  BitNode right;

  public int getData() {
    return data;
  }

  public void setData(int data) {
    this.data = data;
  }

  public BitNode getLeft() {
    return left;
  }

  public void setLeft(BitNode left) {
    this.left = left;
  }

  public BitNode getRight() {
    return right;
  }

  public void setRight(BitNode right) {
    this.right = right;
  }


}
